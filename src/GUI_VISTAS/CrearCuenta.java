public void crearCuenta(){
    Login login = new Login();
    LoginData loginData = new LoginData();
    boolean[] loginRelleno ={false,false};
    
    try{
        
        if (validacionTextField1(jTextFieldUsuario)) {
                login.setUsuario(jTextFieldUsuario.getText());
                loginRelleno[0] = true;
            } else {
                JOptionPane.showMessageDialog(this, "Debe ingresar Nombre Inquilino");
                loginRelleno[0] = false;
            }
        
        if (validacionTextField1(jPasswordField)) {
                login.setContrasenia(Integer.parseInt(jPasswordField.getText()));
                loginRelleno[1] = true;
            } else {
                JOptionPane.showMessageDialog(this, "Debe numeros para la contraseña");
                loginRelleno[1] = false;
            }
        if(loginRelleno[0] == true
             &&loginRelleno[1] == true){
            loginData.guardarLogin(login);
            JOptionPane.showMessageDialog(this, "Usuario y contraseña Creada");
        }
        
        
    }catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Ingrese el numeros correctamente");
            jPasswordField.setText("");
            validacionTextField1(jPasswordField);

        }
}

private boolean validacionTextField1(JTextField jtf) {
        if (jtf.getText().isEmpty()) {
            jtf.setBorder(BorderFactory.createLineBorder(Color.decode("#aa4356")));
            return false;
        } else {
            jtf.setBorder(null);
            return true;
        }
    }

